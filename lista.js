
var carros = ["March", "Fusca", "Monza", "Fit"];

console.log(carros);
console.log(carros.length);

console.log(carros[carros.length] = "Polo"); //inseriu um novo carro no final da lista
console.log(carros);

console.log(carros.push("Variant")) //push insere um novo carro no final da lista
console.log(carros);

console.log(carros.pop()); //retira o último carro da lista
console.log(carros);

console.log(carros.unshift("Gol")); //insere o carro no início da lista
console.log(carros);

console.log(carros.shift()); //retira o carro no início da lista
console.log(carros);


